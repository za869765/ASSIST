-- 後續：當有 LINE 訊息進來且該 user_id 尚未綁定時，admin 從這個名冊裡挑一筆 → 寫入 members 綁定

CREATE TABLE IF NOT EXISTS roster (
  id         INTEGER PRIMARY KEY AUTOINCREMENT,
  real_name  TEXT NOT NULL,
  title      TEXT,                  -- 職稱/位置（例：衛生局股長-東興、龍崎區衛生所-區長）
  zone       TEXT NOT NULL DEFAULT '衛生局',
  user_id    TEXT,                  -- 已綁定的 LINE userId（null = 未綁）
  bound_at   TEXT,
  created_at TEXT DEFAULT (datetime('now'))
);
CREATE INDEX IF NOT EXISTS idx_roster_user_id ON roster(user_id);
CREATE INDEX IF NOT EXISTS idx_roster_zone    ON roster(zone);

